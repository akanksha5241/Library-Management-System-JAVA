    private void txtidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidKeyPressed
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) 
        {
            String mid = txtid.getText();
            
            
            try {
                
                Connection con = DBConnection.getConnection();
                ResultSet rs;

                pst = con.prepareStatement("select * from member where id = ?");
                pst.setString(1, mid);
                rs = pst.executeQuery();
                
                if (rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "Member ID not Found");
                }
                
                else 
                {
                    String membername = rs.getString("name");
                    txtmember.setText(membername.trim());
                }
                
            } catch (SQLException ex) {
                System.getLogger(Issuebook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }//GEN-LAST:event_txtidKeyPressed

    
    public void Issue_Load() {
        int c;

            try {
                pst = con.prepareStatement("select i.id, m.name, b.bname, i.issuedate, i.returndate from issuebook "
                        + "i JOIN member m ON i.memberid = m.id JOIN book b ON i.bookid = b.id");
                rs = pst.executeQuery();

                ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
                c = rsd.getColumnCount();
                

                DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
                d.setRowCount(0);
                        
                while (rs.next()) {
                    Vector v2 = new Vector();

                    for (int i = 1; i <= c; i++) {
                        v2.add(rs.getInt("i.id"));
                        v2.add(rs.getString("m.name"));
                        v2.add(rs.getString("b.bname"));
                        v2.add(rs.getString("i.issuedate"));
                        v2.add(rs.getString("i.returndate"));
                    }
                    d.addRow(v2);
            }

     } catch (Exception e) {
         JOptionPane.showMessageDialog(this, "Load Error");
         e.printStackTrace();
     }
 }

    
    
