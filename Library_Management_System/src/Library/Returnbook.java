    private void txtmidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmidKeyPressed
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) 
        {
        
            String id = txtmid.getText();
        
            try 
            {
                Connection con = DBConnection.getConnection();
                ResultSet rs;

                pst = con.prepareStatement("select m.name, b.bname, i.returndate, DATEDIFF(now(), i.returndate) "
                        + "as elap from issuebook i JOIN member m ON i.memberid = m.id JOIN book b "
                        + "ON i.bookid = b.id and i.memberid = ?");
                    pst.setString(1, id);
                    rs = pst.executeQuery();

                    if (rs.next() == false)
                    {
                        JOptionPane.showMessageDialog(this, "Member ID not Found");
                    }

                    else  
                    {
                        String mname = rs.getString("m.name");
                        String bname = rs.getString("b.bname");

                        txtmname.setText(mname.trim());
                        txtbook.setText(bname.trim());
                        
                        String date = rs.getString("i.returndate");
                        txtrdate.setText(date);
                        
                        String elp = rs.getString("elap");
                        int elaped = Integer.parseInt(elp);
                        
                        if(elaped > 0) 
                        {
                            txtelp.setText(elp);
                            int fine = elaped * 100;
                            txtfine.setText(String.valueOf(fine));
                        }
                        
                        else
                        {
                            txtelp.setText("0");
                            txtfine.setText("0");
                        }
                    }
            }

            catch (SQLException ex) {
                System.getLogger(Returnbook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }//GEN-LAST:event_txtmidKeyPressed

    
    public void Return_Load() {
        int c;

            try {
                pst = con.prepareStatement("select * from returnbook ");
                rs = pst.executeQuery();

                ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
                c = rsd.getColumnCount();
                

                DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
                d.setRowCount(0);
                        
                while (rs.next()) {
                    Vector v2 = new Vector();

                    for (int i = 1; i <= c; i++) {
                        v2.add(rs.getInt("id"));
                        v2.add(rs.getString("mid"));
                        v2.add(rs.getString("mname"));
                        v2.add(rs.getString("bname"));
                        v2.add(rs.getString("returndate"));
                        v2.add(rs.getString("elp"));
                        v2.add(rs.getString("fine"));
                    }
                    d.addRow(v2);
            }

     } catch (Exception e) {
         JOptionPane.showMessageDialog(this, "Load Error");
         e.printStackTrace();
     }
 }
    


    
    
