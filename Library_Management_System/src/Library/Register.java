    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String Username = txtusername.getText();
        String Email = txtemail.getText();
        String Pass = new String(txtpass.getPassword());
        String ConfirmPass = new String(txtconfirmpass.getPassword());

    // Validation
    if (Username.isEmpty() || Email.isEmpty() || Pass.isEmpty() || ConfirmPass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required");
        return;
    }

    if (!Pass.equals(ConfirmPass)) {
        JOptionPane.showMessageDialog(this, "Password and Confirm Password do not match");
        txtpass.setText("");
        txtconfirmpass.setText("");
        return;
    }
    
    try {
        Connection con = DBConnection.getConnection();

        String sql = "INSERT INTO users (username, email, password) VALUES (?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, Username);
        pst.setString(2, Email);
        pst.setString(3, Pass);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Registration Successfully");

        Login l = new Login();
        this.hide();
        l.setVisible(true);
    }
    catch (SQLIntegrityConstraintViolationException e) {

        if (e.getMessage().contains("username")) {
            JOptionPane.showMessageDialog(this, "Username already exists");
        }
        else if(e.getMessage().contains("email")) {
            JOptionPane.showMessageDialog(this, "Email already exists");
        }

        e.printStackTrace();
    }
    
    catch (Exception e) {
        JOptionPane.showMessageDialog(this, "DB Error");
        e.printStackTrace();
    }



    }//GEN-LAST:event_jButton1ActionPerformed

