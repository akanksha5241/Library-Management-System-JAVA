    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String bname = txtbname.getText();
        CategoryItem citem = (CategoryItem) txtcategory.getSelectedItem();
        AuthorItem aitem = (AuthorItem) txtauthor.getSelectedItem();
        PublisherItem pitem = (PublisherItem) txtpublisher.getSelectedItem();
        
        String contents = txtcontent.getText();
        String pages = txtno.getText();
        String edition = txtedition.getText();
        
            if (bname.isEmpty() || citem == null || aitem == null || pitem == null || 
                    contents.isEmpty() || pages.isEmpty() || edition.isEmpty()) 
            {
                JOptionPane.showMessageDialog(this, "All fields are required");
                return;
            }
        
        try {
            Connection con = DBConnection.getConnection();
            ResultSet rs;
            
            String sql = "INSERT INTO book (bname, category, author, publisher, contents, "
                    + "pages, edition) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, bname);
            pst.setInt(2, citem.id);
            pst.setInt(3, aitem.id);
            pst.setInt(4, pitem.id);
            pst.setString(5, contents);
            pst.setString(6, pages);
            pst.setString(7, edition);

            int k = pst.executeUpdate();
            
            
            if(k == 1){
                JOptionPane.showMessageDialog(this, "Book Details Added");
                
                txtbname.setText("");
                txtcategory.setSelectedItem(-1);
                txtauthor.setSelectedItem(-1);
                txtpublisher.setSelectedItem(-1);
                txtcontent.setText("");
                txtno.setText("");
                txtedition.setText("");
                
                Book_Load();
            }
            
            else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        }
        
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "DB Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

